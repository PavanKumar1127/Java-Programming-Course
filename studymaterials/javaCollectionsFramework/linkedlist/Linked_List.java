package javaCollectionsFramework.linkedlist;

/**
 * LinkedList is implemented as a doubly-linked list. In a doubly-linked list,
 * each element, or node, contains a reference to both the next and the previous
 * elements in the sequence.
 */
public class Linked_List {

	// Like ArrayList, a LinkedList can dynamically grow or shrink as needed. It
	// doesn't require specifying the size beforehand.

	// LinkedList excels at insertions and deletions, especially when performed at
	// the beginning or middle of the list. Insertions and deletions involve
	// updating references, and the cost is relatively constant, O(1), for these
	// operations.

	// Unlike arrays or ArrayList, LinkedList has slower random access times because
	// traversing the list to reach a specific index takes O(n) time, where n is the
	// number of elements in the list.

}
