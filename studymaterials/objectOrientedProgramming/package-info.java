package objectOrientedProgramming;

/**
 *
 * Object-Oriented Programming (OOP) organizes code around "objects" that have
 * data (attributes/properties) and code (methods) for data operations. The core
 * concept is to model real-world entities and their interactions in a program.
 *
 *
 * Class is not a real-world entity. It is just a template or blueprint or prototype from which objects are created.
 * Class does not occupy memory.
 * Class is a group of variables of different data types and a group of methods.
 * A Class in Java can contain:
 *	 Data member
 *	 Method
 * 	 Constructor
 *	 Nested Class
 *	 Interface
 */